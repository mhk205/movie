package movie;

public class MovieBean {
	private String mov_name;
	private String mov_actors;
	private String mov_director;
	private String mov_genre;
	private String movieCd;
	private double re_score;
	
	


	public String getMovieCd() {
		return movieCd;
	}
	public void setMovieCd(String movieCd) {
		this.movieCd = movieCd;
	}
	public double getRe_score() {
		return re_score;
	}
	public void setRe_score(double re_score) {
		this.re_score = re_score;
	}
	
	public String getMov_name() {
		return mov_name;
	}
	public void setMov_name(String mov_name) {
		this.mov_name = mov_name;
	}
	public String getMov_actors() {
		return mov_actors;
	}
	public void setMov_actors(String mov_actors) {
		this.mov_actors = mov_actors;
	}
	public String getMov_director() {
		return mov_director;
	}
	public void setMov_director(String mov_director) {
		this.mov_director = mov_director;
	}
	public String getMov_genre() {
		return mov_genre;
	}
	public void setMov_genre(String mov_genre) {
		this.mov_genre = mov_genre;
	}
	



	
}
